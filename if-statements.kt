fun main() {
    //normal if statement 
    val trafficLightColor = "Red"
    if (trafficLightColor == "Red") {
        println("Stop")
    } else if (trafficLightColor == "Yellow") {
        println("Please slow down")
    } else if (trafficLightColor == "Green" ){
        println("Go")   
    }
    else{
        println("Invalid traffic light color")
    }
}